
package br.com.tenorio.stephano.model;


public class Entities {

    private ContextOn contextOn;
    private Card_ card;
    private Comment_ comment;
    private MemberCreator memberCreator;

    public ContextOn getContextOn() {
        return contextOn;
    }

    public void setContextOn(ContextOn contextOn) {
        this.contextOn = contextOn;
    }

    public Card_ getCard() {
        return card;
    }

    public void setCard(Card_ card) {
        this.card = card;
    }

    public Comment_ getComment() {
        return comment;
    }

    public void setComment(Comment_ comment) {
        this.comment = comment;
    }

    public MemberCreator getMemberCreator() {
        return memberCreator;
    }

    public void setMemberCreator(MemberCreator memberCreator) {
        this.memberCreator = memberCreator;
    }

}
